public class Klass {
    private int san;
    private String soz;
    private int[] massiv;



    public  Klass(int san, String soz, int[] massiv) {
        this.san = san;
        this.soz = soz;
        this.massiv = massiv;
    }

    public int getSan() {
        return san;
    }

    public void setSan(int san) {
        this.san = san;
    }

    public String getSoz() {
        return soz;
    }

    public void setSoz(String soz) {
        this.soz = soz;
    }

    public int[] getMassiv() {
        return massiv;
    }

    public void setMassiv(int[] massiv) {
        this.massiv = massiv;
    }
}

